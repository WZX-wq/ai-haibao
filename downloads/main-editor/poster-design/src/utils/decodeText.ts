export function repairKnownMojibake(value?: string) {
  if (typeof value !== 'string' || !value) {
    return value || ''
  }

  let result = value
  for (const [bad, good] of Object.entries(knownMojibakeMap)) {
    result = result.replaceAll(bad, good)
  }
  return result
}

export function decodeTextIfNeeded(value?: string) {
  if (typeof value !== 'string') {
    return value || ''
  }

  const raw = value.trim()
  if (!raw) {
    return ''
  }

  if (raw.includes('%')) {
    try {
      return repairKnownMojibake(decodeURIComponent(raw))
    } catch {
      return repairKnownMojibake(raw)
    }
  }

  return repairKnownMojibake(raw)
}
